  <p>戒子训·诸葛亮</p>
  <p>
  夫君子之行，
  静以修身，
  俭以养德。
  非淡泊无以明志，
  非宁静无以致远。
  <br/>
  夫学需静也，
  才须学也，
  非学无以广才，
  非志无以成学。
  慆慢则不能励精，
  险躁则不能冶性。
  <br/>
  年与时驰，
  意与日去，
  遂成枯落，
  多不接世，
  悲守穷庐，
  将复何及！
  </p>
  <hr/>
  <p>水调歌头·重上井冈山</p>
  <p>
  久有凌云志，
  重上井冈山，
  千里来寻故地，
  旧貌换新颜。
  <br/>
  到处莺歌燕舞，
  更有潺潺流水，
  高路入云端。
  <br/>
  过了黄洋界，
  险处不须看。
  <br/>
  风雷动，
  旌旗奋，
  是人寰。
  <br/>
  三十八年过去，
  弹指一挥间。
  <br/>
  可上九天揽月，
  可下五洋捉鳖。
  谈笑凯歌还。
  <br/>
  世上无难事，
  只要肯攀登。
  </p>
